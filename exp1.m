clear all;
w = linspace(-2*pi, 2*pi, 100000);
z = cos(w) + i*sin(w);
Hz = 1./(1-(0.9.*(z.^-1)));
phase = atan(imag(Hz)./real(Hz));
num = [1,0];
den = [1,-0.9];
Ts = 0.01;
H = tf(num, den, Ts);
subplot(3,1,1);
plot(w,abs(Hz));
xlabel("Frequency");
ylabel("Magnitude");
subplot(3,1,2);
plot(w,phase);
xlabel("Frequency");
ylabel("Phase");
subplot(3,1,3);
pzmap(H);